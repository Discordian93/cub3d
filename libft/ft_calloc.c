#include "libft.h"

void	*ft_calloc(size_t num, size_t size)
{
	void	*ptr;

	if (num == 0 || size == 0)
	{
		num = 1;
		size = 1;
	}
	if (num > SIZE_MAX / size)
		return (NULL);
	ptr = malloc(num * size);
	if (ptr != NULL)
		ft_bzero(ptr, num * size);
	return (ptr);
}
