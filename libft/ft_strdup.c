#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dup;
	size_t	len;

	len = ft_strlen(s1);
	dup = malloc(sizeof(char) * (len + 1));
	if (dup != NULL)
		ft_strlcpy(dup, s1, len + 1);
	return (dup);
}
